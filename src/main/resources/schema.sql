DROP TABLE IF EXISTS users ;
CREATE TABLE users ( id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, name VARCHAR(100) NOT NULL, age integer,salary decimal);
DROP TABLE IF EXISTS department ;
CREATE TABLE department ( id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,user_id integer, name VARCHAR(100) NOT NULL, loc VARCHAR(100));
DROP TABLE IF EXISTS reportstate ;
CREATE TABLE reportstate ( report_state_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,report_status_id integer, report_details_id integer, report_create_date timestamp, report_expire_date timestamp);
DROP TABLE IF EXISTS reportstatus ;
CREATE TABLE reportstatus ( report_status_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,report_status_created timestamp);
DROP TABLE IF EXISTS reportdetails ;
CREATE TABLE reportdetails ( report_details_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,report_more_details VARCHAR(100));
